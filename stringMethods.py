name = "piyush yadav"

print(len(name)) #displays the length of the string
print(name.count("i")) #prints the count of letter in the string
print(name.isalpha()) #checks if the string is alphabet or not
print(name.isnumeric()) #checks if the string is numeric or not
print(name.capitalize()) #capitalizes the first letter of the string
print(name.replace('u','t')) #replaces the character with the desired chararcter
print(name.find('h')) #prints the index number where there is first occurance of the desired character
print(name.upper()) #turns the whole string into upper case
print(name.lower()) #turns the whole string into lower case
print(name*4) #helps in repeating the string multiple times